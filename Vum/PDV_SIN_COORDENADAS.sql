SELECT DISTINCT
CLI.CLI_CODIGO,
CLI.CLI_NOMBRE,
CIU.CIU_NOMBRE AS CIUDAD,
PVC.PDV_CODIGO,
PVC.PVC_NOMBRE_PDV,
PDV.PDV_DIRECCION AS DIRECCION,
PVC.TIP_CODIGO3 AS COD_CANAL,
T1.TIP_NOMBRE AS CANAL,
PDV.TIP_CODIGO AS COD_CADENA,
T2.TIP_NOMBRE AS CADENA,
PAI.PAI_NOMBRE AS PAIS

FROM
SIM_CLIENTE AS CLI
INNER JOIN SIM_ACTIVIDAD AS ACT ON ACT.CLI_CODIGO=CLI.CLI_CODIGO
INNER JOIN SIM_PUNTO_VENTA_CLIENTE AS PVC ON PVC.ACT_CODIGO=ACT.ACT_CODIGO AND PVC.ESTADO=1
INNER JOIN SIM_PUNTO_VENTA AS PDV ON PDV.PDV_CODIGO=PVC.PDV_CODIGO
INNER JOIN SIM_TIPO AS T1 ON T1.TIP_CODIGO=PVC.TIP_CODIGO3
INNER JOIN SIM_TIPO AS T2 ON T2.TIP_CODIGO=PDV.TIP_CODIGO
INNER JOIN SIM_CIUDAD AS CIU ON CIU.CIU_CODIGO=PDV.CIU_CODIGO
INNER JOIN SIM_DEPARTAMENTO AS DEP ON DEP.DEP_CODIGO=CIU.DEP_CODIGO
INNER JOIN SIM_PAIS AS PAI ON PAI.PAI_CODIGO=DEP.PAI_CODIGO AND PAI.PAI_CODIGO=1
WHERE
CLI.CLI_CODIGO IN (212)
AND (PDV.PDV_LATITUD IS NULL AND PDV.PDV_LONGITD IS NULL)
AND (PDV.TIP_CODIGO NOT IN (39995,39996,9594,42084,42112,14634,28875,1511,14012,9593,42085,14635,42113,39688) )
--AND PDV.TIP_CODIGO2 NOT IN (39995,39996,9594,42084,42112,14634,28875,1511,14012,9593,42085,14635,42113))